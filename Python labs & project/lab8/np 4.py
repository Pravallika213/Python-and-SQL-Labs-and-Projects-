import numpy as np

# Input list
my_list = [1, 2, 3, 4, 5, 6, 7, 8]

# Input tuple
my_tuple = ([8, 4, 6], [1, 2, 3])

# Converting list to array
list_array = np.array(my_list)

# Converting tuple to array
tuple_array = np.array(my_tuple)

print("List to array:")
print(list_array)

print("Tuple to array:")
print(tuple_array)
